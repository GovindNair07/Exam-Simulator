    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id = jTextField19.getText();
        String oldPassword = new String(jPasswordField1.getPassword());
        String newPassword = new String(jPasswordField2.getPassword());
        String confirmNewPassword = new String(jPasswordField3.getPassword());

        boolean result = dao.verification(id, oldPassword);

        if (result) {

            if (newPassword.equals(confirmNewPassword)) {

                dao.changePassword(id, newPassword);

                JOptionPane.showMessageDialog(
                    this,
                    "Hello " + LoginWindow.id +
                    "\nYour password is changed." +
                    "\nPlease login again"
                );

                this.dispose();
                new LoginWindow().setVisible(true);

            } else {
                jLabel3.setText("new password & confirm new password doesn't match");
            }

        } else {
            jLabel3.setText("invalid id / old password");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

