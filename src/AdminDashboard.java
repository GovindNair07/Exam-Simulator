    public void loadData() {

        List<Student> list = dao.showUserData();
        

        dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0); 

        jComboBox2.addItem("Select id");

        for (Student student : list) {

            Object obj[] = {
                
                student.getId(),
                student.getPassword(),
                student.getFullName(),
                student.getCourse(),
                student.getContact(),
                student.getDob(),
                student.getCountry(),
                student.getCity(),
                student.getAddress(),
                student.getMailid()
            };

            dtm.addRow(obj);
            jComboBox2.addItem(student.getId());
        }
    }
    
    
    public void loadQuestion() {

        List<Question> list = dao.showQuestion();
        

        dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0); 

        jComboBox3.addItem("Select Serial No");

        for (Question question : list) {

            Object obj[] = {
                
                question.getSerialNo(),
                question.getTechId(),
                question.getTechnology(),
                question.getQuestion(),
                question.getOption1(),
                question.getOption2(),
                question.getOption3(),
                question.getOption4(),
                question.getCorrectOption(),
                
            };

            dtm.addRow(obj);
            jComboBox3.addItem(""+question.getSerialNo());
           
        }
    }


